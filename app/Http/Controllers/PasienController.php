    {
        $request->validate([
            'nama'     => 'required',
            'alamat'   => 'required',
            'no_telp'  => 'required',
            'rt'       => 'required',
            'tgl_lahir' => 'required',
        ]);

        $pasien = Pasien::create([
            'nama'          => $request->nama,
            'alamat'         => $request->alamat,
            'no_telp'       => $request->no_telp,
            'rt'              => $request->rt,
            'tgl_lahir'       => $request->tgl_lahir,
        ]);


        return redirect('operator');
    }

